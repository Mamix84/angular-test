/**
 * Export
 */
var exports = module.exports = {}

/**
 * Sum function
 */
exports.sum = function(num_a, num_b){
	return num_a + num_b
}

/**
 * Subtract function
 */
exports.subtract = function(num_a, num_b){
	return num_a - num_b
}

/**
 * Multiplication function
 */
exports.multiply = function(num_a, num_b){
	return num_a * num_b
}

/**
 * Division function
 */
exports.divide = function(num_a, num_b){
	return num_a / num_b
}